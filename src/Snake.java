
public class Snake {
	private Head head;
	private Body body;
	
	public Snake(Head head, Body body) {
		this.head = head;
		this.body = body;
	}
	public Head getHead() {
		return this.head;
	}
	public void setHead(Head head) {
		this.head = head;
	}
	public Body getBody() {
		return this.body;
	}
	public void setBody(Body body) {
		this.body = body;
	}	
	
}
